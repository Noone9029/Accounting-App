#!/usr/bin/env node

const fs = require("node:fs");
const path = require("node:path");

const DEFAULT_EVIDENCE_PATH = path.join(process.cwd(), "docs", "security", "evidence", "ENV_SEPARATION_CHECK.md");
const DEFAULT_JSON_PATH = path.join(process.cwd(), "docs", "security", "evidence", "ENV_SEPARATION_CHECK.json");

const ENV_GROUPS = {
  runtime: ["DATABASE_URL"],
  migrationAdmin: ["DIRECT_URL", "MIGRATION_DATABASE_URL", "PRISMA_MIGRATION_DATABASE_URL"],
  readOnly: ["READONLY_DATABASE_URL", "REPORTING_DATABASE_URL"],
  supabase: ["SUPABASE_URL", "SUPABASE_ANON_KEY", "SUPABASE_SERVICE_ROLE_KEY", "SUPABASE_ACCESS_TOKEN"],
  vercel: ["VERCEL", "VERCEL_ENV", "VERCEL_URL", "VERCEL_TOKEN", "VERCEL_ORG_ID", "VERCEL_PROJECT_ID"],
  smtp: ["SMTP_HOST", "SMTP_USER", "SMTP_PASSWORD", "SMTP_PORT", "RESEND_API_KEY"],
  providers: ["STRIPE_SECRET_KEY", "PAYMENT_PROVIDER_API_KEY", "BANK_PROVIDER_API_KEY", "S3_ACCESS_KEY_ID", "S3_SECRET_ACCESS_KEY"],
  compliance: ["ZATCA_PRIVATE_KEY", "ZATCA_OTP", "UAE_ASP_API_KEY", "UAE_ASP_WEBHOOK_SECRET", "PEPPOL_PROVIDER_API_KEY"],
  auth: ["JWT_SECRET", "SESSION_SECRET", "AUTH_SECRET"],
};

function parseArgs(argv = process.argv) {
  const options = { json: false, markdown: false, ci: false, write: true, evidencePath: DEFAULT_EVIDENCE_PATH, jsonPath: DEFAULT_JSON_PATH };
  for (let index = 2; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--json") {
      options.json = true;
    } else if (arg === "--markdown") {
      options.markdown = true;
    } else if (arg === "--ci") {
      options.ci = true;
    } else if (arg === "--no-write") {
      options.write = false;
    } else if (arg === "--out") {
      options.evidencePath = path.resolve(argv[index + 1] || "");
      index += 1;
    } else if (arg.startsWith("--out=")) {
      options.evidencePath = path.resolve(arg.slice("--out=".length));
    } else if (arg === "--json-out") {
      options.jsonPath = path.resolve(argv[index + 1] || "");
      index += 1;
    } else if (arg.startsWith("--json-out=")) {
      options.jsonPath = path.resolve(arg.slice("--json-out=".length));
    }
  }
  return options;
}

function buildCheck(env = process.env) {
  const groups = {};
  for (const [group, names] of Object.entries(ENV_GROUPS)) {
    groups[group] = names.map((name) => ({ name, present: Object.prototype.hasOwnProperty.call(env, name) && Boolean(env[name]) }));
  }

  const presentNames = Object.values(groups)
    .flat()
    .filter((entry) => entry.present)
    .map((entry) => entry.name)
    .sort();

  const warnings = [];
  if (isPresent(env, "DATABASE_URL") && !isPresent(env, "DIRECT_URL") && !isPresent(env, "MIGRATION_DATABASE_URL")) {
    warnings.push("DATABASE_URL is present but no separate DIRECT_URL or MIGRATION_DATABASE_URL name is present.");
  }
  if (isPresent(env, "DIRECT_URL") && isPresent(env, "DATABASE_URL")) {
    warnings.push("DATABASE_URL and DIRECT_URL are both present; verify runtime code never uses DIRECT_URL.");
  }
  if (Object.keys(env).some((name) => /SERVICE_ROLE|PRIVATE_KEY|SECRET|TOKEN|PASSWORD|API_KEY/.test(name)) && process.env.NODE_ENV === "production") {
    warnings.push("Sensitive credential env names are present in a production-like process; verify values stay in server-only secret stores.");
  }

  return {
    status: warnings.length ? "REVIEW_REQUIRED" : "NO_ENV_SEPARATION_WARNINGS",
    generatedBy: "scripts/security-env-separation-check.cjs",
    deterministic: true,
    noDatabaseConnection: true,
    noNetwork: true,
    noMutation: true,
    valuesPrinted: false,
    groups,
    presentNames,
    warnings,
    idealSeparation: [
      "API runtime should use DATABASE_URL with a least-privilege runtime role.",
      "Migration/admin work should use DIRECT_URL or MIGRATION_DATABASE_URL only in controlled migration windows.",
      "Read-only diagnostics should use READONLY_DATABASE_URL only after explicit non-production approval.",
      "Browser/web runtime must not receive service-role, DIRECT_URL, private key, ASP, ZATCA, SMTP, payment, or storage secret values.",
    ],
  };
}

function isPresent(env, name) {
  return Object.prototype.hasOwnProperty.call(env, name) && Boolean(env[name]);
}

function formatMarkdown(report) {
  const lines = [
    "# Environment Separation Check Evidence",
    "",
    "Status: read-only diagnostic evidence",
    "Generated by: `scripts/security-env-separation-check.cjs`",
    "",
    "## Safety Contract",
    "",
    "- Environment variable names are inspected by category.",
    "- Values are never printed, partially printed, parsed, or validated.",
    "- No database connection.",
    "- No network calls.",
    "- No mutation.",
    "",
    "## Summary",
    "",
    `- Status: \`${report.status}\``,
    `- Present configured names: ${report.presentNames.length}`,
    `- Values printed: ${report.valuesPrinted}`,
    "",
    "## Groups",
    "",
    "| Group | Names present | Names absent |",
    "| --- | --- | --- |",
    ...Object.entries(report.groups).map(([group, entries]) => {
      const present = entries.filter((entry) => entry.present).map((entry) => entry.name).join(", ") || "-";
      const absent = entries.filter((entry) => !entry.present).map((entry) => entry.name).join(", ") || "-";
      return `| ${group} | ${present} | ${absent} |`;
    }),
    "",
    "## Warnings",
    "",
    ...formatList(report.warnings),
    "",
    "## Ideal Separation",
    "",
    ...report.idealSeparation.map((item) => `- ${item}`),
    "",
  ];
  return `${lines.join("\n").replace(/\n+$/, "")}\n`;
}

function formatList(items) {
  return items.length ? items.map((item) => `- ${item}`) : ["No entries."];
}

function writeEvidence(report, options = {}) {
  const evidencePath = options.evidencePath || DEFAULT_EVIDENCE_PATH;
  const jsonPath = options.jsonPath || DEFAULT_JSON_PATH;
  fs.mkdirSync(path.dirname(evidencePath), { recursive: true });
  fs.writeFileSync(evidencePath, formatMarkdown(report));
  fs.writeFileSync(jsonPath, `${JSON.stringify(report, null, 2)}\n`);
}

if (require.main === module) {
  const options = parseArgs();
  const report = buildCheck(process.env);
  if (options.write) {
    writeEvidence(report, options);
  }
  if (options.json) {
    process.stdout.write(`${JSON.stringify(report, null, 2)}\n`);
  } else if (options.markdown) {
    process.stdout.write(formatMarkdown(report));
  } else {
    process.stdout.write(`Environment separation check: ${report.status}; presentNames=${report.presentNames.length}; evidence=${options.evidencePath}\n`);
  }
  if (options.ci && report.status === "REVIEW_REQUIRED") {
    process.exitCode = 0;
  }
}

module.exports = {
  ENV_GROUPS,
  buildCheck,
  formatMarkdown,
  parseArgs,
};
